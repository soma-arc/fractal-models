export async function exportPng(
  canvas: HTMLCanvasElement,
  filename: string,
): Promise<void> {
  const url = canvas.toDataURL('image/png');
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
}
